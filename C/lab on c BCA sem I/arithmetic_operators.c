// Write a program to demonstrate Arithmetic Operators in C.
#include<stdio.h>
int main(){
    int a = 4, b = 8;
    printf("Arithmetic Operations in  C\n");
    printf("a + b = %d\n",a+b);
    printf("a - b = %d\n",a-b);
    printf("a * b = %d\n",a*b);
    printf("a / b = %d\n",a/b);
    printf("a %% b = %d",a%b);
    return 0;
}